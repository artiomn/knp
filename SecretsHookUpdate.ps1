#Requires -Version 3.0

Param (
    [Parameter(Mandatory=$true)]
    [ValidateSet('Repository', 'Global', 'OnlyNew', 'Update')]
    [string]$Mode = 'Repository',

    [Parameter()]
    [String]$ShellArgs
)

$ErrorActionPreference = "Stop"
$ProgressPreference = 'SilentlyContinue'

if ($Mode -ne "Update") {
    $TimeoutSec = 100
} else {
    $TimeoutSec = 3
}

$UpdateScriptFileName = "SecretsHookUpdate.ps1"
$InstallScriptFileName = "SecretsHookUpdateCore.ps1"
$VersionFileName = "version.txt"
$SecretIndexDir = "$env:userprofile/.secretindex"
$ScriptPath = "$SecretIndexDir/$InstallScriptFileName"
$VersionPath = "$SecretIndexDir/$VersionFileName"

if (-not (Test-Path $SecretIndexDir)) {
    New-Item -ItemType Directory -Path $SecretIndexDir | Out-Null
}

$CurrentVersion = ""
if (Test-Path $VersionPath) {
    $CurrentVersion = Get-Content $VersionPath
}

$VersionInfoUrl="https://secret-api-anon-prod.ido-services.avp.ru/api/hookversions/scripts/latest"
$VersionInfo = (Invoke-WebRequest -Uri $VersionInfoUrl -UseBasicParsing -TimeoutSec $TimeoutSec).Content | ConvertFrom-Json
if (!$VersionInfo.version){
    Write-Error "Can't find version number in version info"
    Exit -1
}
if (!$VersionInfo.address){
    Write-Error "Can't find scripts uri in version info"
    Exit -1
}

$LatestVersion = $VersionInfo.version
$ScriptsUri = $VersionInfo.address
if ($Mode -ne "Update") {
    Write-Host "Downloading secret hook update script..."
    Invoke-WebRequest -Uri "$ScriptsUri/$UpdateScriptFileName" -OutFile "$SecretIndexDir/$UpdateScriptFileName"
}

if (-not (Test-Path -Path $ScriptPath) -or ($CurrentVersion -ne $LatestVersion)) {
    Write-Host "Downloading secret hook installation script..."
    Invoke-WebRequest -Uri "$ScriptsUri/$InstallScriptFileName" -OutFile $ScriptPath -TimeoutSec $TimeoutSec
    Set-Content -Path $VersionPath -Value $LatestVersion -NoNewline
}

$ShellArgs = $ShellArgs.Replace('"','\"');

powershell.exe -ExecutionPolicy Bypass -File $ScriptPath -Mode $Mode -ShellArgs:$ShellArgs
